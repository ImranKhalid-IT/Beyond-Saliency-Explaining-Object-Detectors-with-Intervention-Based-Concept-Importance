DEVICE = 'cuda' if torch.cuda.is_available() else 'cpu'

Box = Tuple[int, int, int, int]

# --- UTILITY OPERATIONS ---
def load_rgb_image(path: str | Path) -> np.ndarray:
    return np.asarray(Image.open(path).convert("RGB"))

def robust_normalize(heatmap: np.ndarray) -> np.ndarray:
    finite_values = heatmap[np.isfinite(heatmap)]
    if finite_values.size == 0: return np.zeros_like(heatmap, dtype=np.float32)
    lower, upper = np.percentile(finite_values, 1.0), np.percentile(finite_values, 99.0)
    if upper <= lower: return np.zeros_like(heatmap, dtype=np.float32)
    return np.clip((heatmap - lower) / (upper - lower), 0.0, 1.0).astype(np.float32)

def union_box(box_i: Box, box_j: Box) -> Box:
    return (min(box_i[0], box_j[0]), min(box_i[1], box_j[1]), max(box_i[2], box_j[2]), max(box_i[3], box_j[3]))

# --- DETECTOR CORE PROCESSOR WITH GRAPH CAPTURE ---
class CoreYOLOProcessor:
    def __init__(self, device='cpu'):
        self.device = device
        self.feats = None
        self.grads = None
        self.model = torch.hub.load('ultralytics/yolov5', 'yolov5l', pretrained=True, trust_repo=True).to(device)
        self.hook_layer = self.model.model.model.model[-2]

        # Double backend for clean segmentation masks
        seg_w = DeepLabV3_ResNet101_Weights.DEFAULT
        self.seg_model = deeplabv3_resnet101(weights=seg_w).to(device).eval()
        self.seg_trans = seg_w.transforms()
        self.seg_cats = seg_w.meta["categories"]

    def _forward_hook(self, module, input, output):
        self.feats = output
    def _backward_hook(self, module, grad_in, grad_out):
        self.grads = grad_out[0]

    def process(self, pil_img: Image.Image):
        self.model.eval()
        h_f = self.hook_layer.register_forward_hook(self._forward_hook)
        h_b = self.hook_layer.register_backward_hook(self._backward_hook)
        results = self.model(pil_img)
        h_f.remove(); h_b.remove()

        dets = [{'box_xyxy': np.array([r.xmin, r.ymin, r.xmax, r.ymax]), 'score': float(r.confidence),
                 'label': int(r['class']), 'name': self.model.names[int(r['class'])]}
                for _, r in results.pandas().xyxy[0].iterrows()]
        return dets, self.feats.clone().detach()

    def segment(self, pil_img: Image.Image, targets: List[str]) -> np.ndarray:
        t = self.seg_trans(pil_img).unsqueeze(0).to(self.device)
        with torch.no_grad(): out = self.seg_model(t)['out'][0].argmax(0).cpu().numpy()
        mask = np.zeros_like(out, dtype=np.uint8)
        for n in targets:
            if n in self.seg_cats: mask[out == self.seg_cats.index(n)] = 1
        return cv2.resize(mask, pil_img.size, interpolation=cv2.INTER_NEAREST)

