# 1. RCE (Ours) Causal Concept Execution (UPDATED FOR STRICT RELATION)
def run_rce_ours(features, box1, box2, orig_size, processor, pil_img, target_names):
    _, C, Hf, Wf = features.shape
    W_img, H_img = orig_size

    # 1. Union Box for processing
    u_box = union_box(box1, box2)
    fx1, fy1 = int(u_box[0]/W_img*Wf), int(u_box[1]/H_img*Hf)
    fx2, fy2 = int(u_box[2]/W_img*Wf), int(u_box[3]/H_img*Hf)

    box_mask = torch.zeros(Hf, Wf, device=features.device, dtype=torch.bool)
    if fy1 < fy2 and fx1 < fx2: box_mask[fy1:fy2, fx1:fx2] = True

    A = features.permute(0, 2, 3, 1).reshape(-1, C)
    flat_mask = box_mask.flatten()
    if not flat_mask.any(): return np.zeros((H_img, W_img))

    kmeans = KMeans(n_clusters=5, random_state=42, n_init='auto').fit(A[flat_mask].cpu().numpy())
    centers = torch.from_numpy(kmeans.cluster_centers_).to(features.device).float()
    sim = F.softmax(-torch.cdist(torch.relu(A), centers) / 0.05, dim=1).T.reshape(1, 5, Hf, Wf)

    # --- NAYA IZAFA: Relational Interaction Mask ---
    # دونوں آبجیکٹس کا الگ الگ ماسک نکال کر ان کا درمیانی (Interaction) حصہ نکالیں
    mask1 = processor.segment(pil_img, [target_names[0]])
    mask2 = processor.segment(pil_img, [target_names[1]])

    kernel = np.ones((65, 65), np.uint8) # آپ اس ویلیو کو کم یا زیادہ کر کے ریلیشن ایریا کنٹرول کر سکتے ہیں
    dilated1 = cv2.dilate(mask1, kernel, iterations=1)
    dilated2 = cv2.dilate(mask2, kernel, iterations=1)

    # صرف وہ جگہ جہاں دونوں آبجیکٹس مل رہے ہیں
    relational_mask = (dilated1 * dilated2).astype(np.float32)
    relational_mask = cv2.GaussianBlur(relational_mask, (41, 41), 0)

    if relational_mask.max() > 0:
        relational_mask /= relational_mask.max()

    # فائنل ہیٹ میپ بنانا
    final_overlay = np.zeros((H_img, W_img), dtype=np.float32)
    for i in range(min(3, sim.shape[1])):
        m_res = cv2.resize(sim[0, i].cpu().numpy(), (W_img, H_img))
        final_overlay += m_res

    # ہیٹ میپ کو صرف ریلیشن والی جگہ پر محدود کریں
    final_overlay = final_overlay * relational_mask
    return final_overlay

# 2. CRAFT (Fel et al., 2023) Matrix Factorization Logic
def run_craft(features, box, orig_size):
    _, C, Hf, Wf = features.shape
    A = torch.relu(features).permute(0, 2, 3, 1).reshape(-1, C).cpu().numpy()
    kmeans = KMeans(n_clusters=6, random_state=0, n_init='auto').fit(A)
    best_concept = kmeans.labels_.reshape(Hf, Wf)
    hm = (best_concept == np.argmax(np.bincount(kmeans.labels_))).astype(np.float32)
    return cv2.resize(hm, orig_size, interpolation=cv2.INTER_LINEAR)

# 3. Finer-CAM (Zhang et al., 2025) Contrastive Attributions
def run_finer_cam(features, box, orig_size):
    _, C, Hf, Wf = features.shape
    W_img, H_img = orig_size
    fx1, fy1 = int(box[0]/W_img*Wf), int(box[1]/H_img*Hf)
    fx2, fy2 = int(box[2]/W_img*Wf), int(box[3]/H_img*Hf)

    fg = torch.zeros(1, 1, Hf, Wf, device=features.device)
    if fy1 < fy2 and fx1 < fx2: fg[:, :, fy1:fy2, fx1:fx2] = 1
    w = (features * fg).sum(dim=[2,3])/(fg.sum()+1e-8) - (features * (1-fg)).sum(dim=[2,3])/((1-fg).sum()+1e-8)
    cam = F.relu(torch.sum(features * w.view(1, C, 1, 1), dim=1).squeeze(0))
    return cv2.resize(cam.cpu().numpy(), orig_size, interpolation=cv2.INTER_LINEAR)

# 4. D-RISE (Petsiuk et al., 2021) Object-level Perturbation Execution
def run_drise(image_path, processor, target_box, num_masks=40):
    pil_img = Image.open(image_path).convert('RGB')
    W, h = pil_img.size
    accum_map = np.zeros((h, W), dtype=np.float32)

    for _ in range(num_masks):
        mask_low = np.random.rand(8, 8) > 0.5
        mask = cv2.resize(mask_low.astype(np.float32), (W, h), interpolation=cv2.INTER_LINEAR)

        perturbed = (np.array(pil_img) * mask[:, :, None]).astype(np.uint8)
        dets, _ = processor.process(Image.fromarray(perturbed))

        for d in dets:
            if d['label'] == target_box['label']:
                x1, y1, x2, y2 = max(d['box_xyxy'][0], target_box['box_xyxy'][0]), max(d['box_xyxy'][1], target_box['box_xyxy'][1]), min(d['box_xyxy'][2], target_box['box_xyxy'][2]), min(d['box_xyxy'][3], target_box['box_xyxy'][3])
                if (x2-x1) > 0 and (y2-y1) > 0:
                    accum_map += mask * d['score']
                    break
    return accum_map

# 5. ODAM (Zhao & Chan, ICLR 2023) True Gradient-based Implementation
def run_odam(processor, features, target_box, orig_size):
    if processor.grads is not None:
        alpha = processor.grads.mean(dim=[2, 3], keepdim=True)
        odam_map = F.relu(torch.sum(alpha * features, dim=1)).squeeze(0).cpu().numpy()
        return cv2.resize(odam_map, orig_size, interpolation=cv2.INTER_LINEAR)
    return cv2.resize(features.mean(dim=1).squeeze(0).cpu().numpy(), orig_size)


