# --- 2. MODEL & CONCEPT EXTRACTION CLASSES ---
class YOLOv5_Processor:
    """Handles YOLOv5 model loading, prediction, and feature extraction."""
    def __init__(self, model_name='yolov5l', device='cpu'):
        self.device = device
        self.captured_features = None
        # Load model
        self.model = torch.hub.load(
            'ultralytics/yolov5',
            model_name,
            pretrained=True,
            trust_repo=True
        ).to(self.device).eval()
        # The layer from which to extract features (layer 8 in YOLOv5l's backbone)
        self.hook_layer = self.model.model.model.model[8]

    def _feature_hook(self, module, input, output):
        """A hook to capture the output of a specific layer."""
        self.captured_features = output

    def get_detections_and_features(self, pil_image: Image.Image, conf_thresh=0.5, iou_thresh=0.45):
        """Runs detection and captures intermediate feature maps."""
        hook = self.hook_layer.register_forward_hook(self._feature_hook)
        self.model.conf = conf_thresh
        self.model.iou = iou_thresh

        results = self.model(pil_image)
        hook.remove()

        if self.captured_features is None:
            raise RuntimeError("Feature extraction failed.")

        dets = []
        df = results.pandas().xyxy[0]
        class_names = self.model.names
        for _, r in df.iterrows():
            class_idx = int(r['class'])
            dets.append({
                'box_xyxy': np.array([int(r.xmin), int(r.ymin), int(r.xmax), int(r.ymax)]),
                'score': float(r.confidence),
                'label_idx': class_idx,
                'class_name': class_names[class_idx]
            })

        features = self.captured_features.clone().detach()
        return dets, features

    def generate_finer_cam_heatmap(self, box_xyxy: np.ndarray, pil_size: Tuple[int, int]):
        """Generates a Finer-CAM heatmap to show where the model focuses."""
        features = self.captured_features
        if features is None:
            raise RuntimeError("Features not captured. Run get_detections_and_features first.")

        _, C, Hf, Wf = features.shape
        W_img, H_img = pil_size

        # Map image coordinates to feature map coordinates
        fx1, fy1 = int(box_xyxy[0] / W_img * Wf), int(box_xyxy[1] / H_img * Hf)
        fx2, fy2 = int(box_xyxy[2] / W_img * Wf), int(box_xyxy[3] / H_img * Hf)

        # Create foreground and background masks
        fg_mask = torch.zeros(Hf, Wf, device=features.device)
        if fy1 < fy2 and fx1 < fx2:
            fg_mask[fy1:fy2, fx1:fx2] = 1
        bg_mask = 1 - fg_mask

        # Calculate weights by contrasting foreground and background features
        fg_feats = (features * fg_mask).sum(dim=[2, 3]) / (fg_mask.sum() + 1e-8)
        bg_feats = (features * bg_mask).sum(dim=[2, 3]) / (bg_mask.sum() + 1e-8)
        weights = (fg_feats - bg_feats).view(1, C, 1, 1)

        # Generate CAM
        finer_cam = torch.sum(features * weights, dim=1).squeeze()
        finer_cam = torch.relu(finer_cam)

        # Normalize and resize
        if finer_cam.max() > 0:
            finer_cam = (finer_cam - finer_cam.min()) / finer_cam.max()

        return cv2.resize(finer_cam.cpu().numpy(), pil_size)

class CRAFT_Concept_Explainer:
    """Handles NMF-based concept extraction from feature maps."""
    def __init__(self, K, nmf_iter=200, device='cpu'):
        self.K = K
        self.device = device
        self.nmf_iter = nmf_iter
        self.W = None # Concept basis vectors (D x K)

    def fit_nmf(self, feats: torch.Tensor, box: np.ndarray, orig_size: Tuple[int, int]):
        """Fits NMF on features within a bounding box to find concepts."""
        B, C, Hf, Wf = feats.shape
        W_img, H_img = orig_size
        fx1, fy1 = int(box[0] / W_img * Wf), int(box[1] / H_img * Hf)
        fx2, fy2 = int(box[2] / W_img * Wf), int(box[3] / H_img * Hf)

        mask = torch.zeros(Hf, Wf, device=feats.device)
        if fy1 < fy2 and fx1 < fx2:
            mask[fy1:fy2, fx1:fx2] = 1

        A = feats.permute(0, 2, 3, 1).reshape(-1, C)
        A_masked = A[mask.flatten() > 0]
        A_masked_non_negative = torch.relu(A_masked).cpu().numpy()

        if A_masked_non_negative.shape[0] < self.K:
              print("Warning: Not enough feature vectors in the box to extract concepts. Skipping NMF.")
              return False

        nmf = NMF(n_components=self.K, max_iter=self.nmf_iter, init='nndsvda', random_state=42)
        nmf.fit(A_masked_non_negative)
        self.W = torch.from_numpy(nmf.components_).to(self.device).float()
        return True

    def get_spatial_activations(self, feats: torch.Tensor):
        """Calculates the spatial activation map for each concept."""
        if self.W is None:
            raise RuntimeError("NMF model has not been fit. Call fit_nmf() first.")
        B, C, Hf, Wf = feats.shape
        A = feats.permute(0, 2, 3, 1).reshape(B * Hf * Wf, C)
        A_non_negative = torch.relu(A)
        # Project features onto concept basis to get activations
        U = A_non_negative @ torch.linalg.pinv(self.W)
        return U.reshape(B, Hf, Wf, self.K).permute(0, 3, 1, 2)

# --- 3. Novel Causal Intervention Module ---
class YoloCausalExplainer:
    """
    Implements a robust causal intervention mechanism by ablating concepts
    from the input image and re-running the full model.
    """
    def __init__(self, processor: YOLOv5_Processor):
        self.processor = processor

    def run_intervention(self, pil_image: Image.Image, original_detections: List[Dict], spatial_activations: torch.Tensor, target_det: Dict):
        """
        Runs intervention by ablating each concept from the input image and
        measuring the drop in the target object's detection score.
        """
        causal_effects = {}
        original_score = target_det['score']
        target_label_idx = target_det['label_idx']

        B, K, Hf, Wf = spatial_activations.shape
        W_img, H_img = pil_image.size

        for k_concept in range(K):
            # Step 1: Get the concept activation map and create a mask
            concept_map = spatial_activations[0, k_concept].cpu().numpy()
            map_resized = cv2.resize(concept_map, (W_img, H_img))

            if map_resized.max() > 0:
                map_norm = (map_resized - map_resized.min()) / (map_resized.max() - map_resized.min())
            else:
                map_norm = map_resized

            # Create a binary mask for pixels where concept activation is high
            ablation_mask_np = (map_norm > np.percentile(map_norm, 85)).astype(np.uint8)
            ablation_mask_pil = Image.fromarray(ablation_mask_np * 255, 'L')

            # Step 2: Create the ablated image by blurring the concept area
            img_blurred = pil_image.filter(ImageFilter.GaussianBlur(radius=15))
            img_ablated = Image.composite(img_blurred, pil_image, ablation_mask_pil)

            # Step 3: Re-run detection on the ablated image
            new_detections, _ = self.processor.get_detections_and_features(img_ablated)

            # Step 4: Find the new score for the target object
            new_score = 0.0
            # Find all newly detected objects with the same class label
            new_target_dets = [d for d in new_detections if d['label_idx'] == target_label_idx]
            if new_target_dets:
                # Assume the highest-scoring one is our target
                best_new_det = max(new_target_dets, key=lambda d: d['score'])
                new_score = best_new_det['score']

            score_drop = original_score - new_score
            causal_effects[k_concept] = score_drop

        return causal_effects

